
public class TypeCasting {

	public static void main(String[] args) {
		
		int x = 5;
		
		// Implicit casting 
		long y = x;
		
		System.out.println(y);
		
		// byte -> Short -> char -> int -> long -> float -> double

		
		// Narrowing Typecasting, Explicit Type cast
		byte z =  (byte) x;
		// double -> float -> long -> int -> char -> short -> byte
	}
}
