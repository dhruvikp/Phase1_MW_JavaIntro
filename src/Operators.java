
public class Operators {

	public static void main(String[] args) {
		
		
		int a  = 5;
		
		// a = a+1;
		System.out.println(++a); // 6

		System.out.println(a++); //6 
		System.out.println(a);	//7
	
	
		
		boolean a1 = true;
		
		boolean b1 = false;
		
		// Condiditonal operators
		System.out.println(a1 || b1);
		
		int a2=5;
		int b2=4;
		
		// Relational operators
		System.out.println(a2>b2);
	
	
	}
	

}
